int		ft_atoi(const char *str)
{
	int i;
	int j;

	i = 0;
	j = 1;
	while (str[i] == ' ' || str[i] == '\t' || str[i] == '\v' ||
			str[i] == '\n' || str[i] == '\r' || str[i] == '\f')
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		j = (str[i] == '-') ? -1 : 1;
		i++;
	}
	if (str[i] <= '9' && str[i] >= '0')
		j = (str[i] - '0') * j;
	else
		return (0);
	i++;
	while (str[i] <= '9' && str[i] >= '0')
	{
		j = (j < 0) ? (j * 10) - (str[i] - '0') : (j * 10) + (str[i] - '0');
		i++;
	}
	return (j);
}
